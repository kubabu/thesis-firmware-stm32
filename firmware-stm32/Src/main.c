void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	if (GPIO_Pin == MPU6050_INT_Pin) {
		// I2C communication uses interrupts, ext interrupt handler can only set flag
		IMU_Sensor_UpdateInterruptFlag(imu, SENSOR_DATA_READY);
	}
}

